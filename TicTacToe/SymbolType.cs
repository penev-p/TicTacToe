namespace TicTacToe
{
    /// <summary>
    /// Type of a symbol(free, X or O) for every cell
    /// </summary>
    public enum SymbolType
    {
        /// <summary>
        /// The cell is empty
        /// </summary>
        Free,
        /// <summary>
        /// The cell value is X
        /// </summary>
        X,
        /// <summary>
        /// The cell value is O
        /// </summary>
        O
    }
}
